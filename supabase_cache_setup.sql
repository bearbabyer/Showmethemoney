-- ============================================================
-- 13. 統一啟用 RLS + 建立 policies
--     - 讀：anon + authenticated 皆可（公用快取）
--     - 寫：僅 authenticated（登入使用者可幫忙回寫快取）
-- ============================================================
do $$
declare
  tbl text;
  tbls text[] := array[
    'api_cache','market_indices','stock_daily','stock_quotes',
    'stock_fundamentals','institutional_flow','three_institutionals',
    'business_cycle','news_items','recommendation_snapshots',
    'translations','fin_calendar'
  ];
begin
  foreach tbl in array tbls loop
    execute format('alter table public.%I enable row level security', tbl);

    execute format('drop policy if exists "%s_select_all" on public.%I', tbl, tbl);
    execute format('drop policy if exists "%s_insert_auth" on public.%I', tbl, tbl);
    execute format('drop policy if exists "%s_update_auth" on public.%I', tbl, tbl);

    execute format(
      'create policy "%s_select_all" on public.%I for select to anon, authenticated using (true)',
      tbl, tbl
    );
    execute format(
      'create policy "%s_insert_auth" on public.%I for insert to authenticated with check (true)',
      tbl, tbl
    );
    execute format(
      'create policy "%s_update_auth" on public.%I for update to authenticated using (true) with check (true)',
      tbl, tbl
    );
    -- 不提供 DELETE policy；如需清理由管理員於 SQL Editor 手動處理
  end loop;
end$$;


-- ============================================================
-- 14. 便利視圖：近 30 天個股均價 / 每日更新摘要（範例）
-- ============================================================
create or replace view public.v_stock_latest as
  select distinct on (stock_code)
         stock_code, trade_date, close, change_pct, volume
    from public.stock_daily
   order by stock_code, trade_date desc;

create or replace view public.v_news_latest as
  select source, category, title, coalesce(title_zh, title) as title_display,
         url, published_at, score
    from public.news_items
   order by published_at desc nulls last
   limit 500;


