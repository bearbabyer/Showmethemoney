  price       numeric(12,4),
  prev_close  numeric(12,4),
  change      numeric(12,4),
  change_pct  numeric(8,4),
  volume      bigint,
  market      text,                    -- 'tse' | 'otc'
  fetched_at  timestamptz not null default now(),
  raw         jsonb
