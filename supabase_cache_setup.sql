-- ============================================================
-- 9. 新聞條目（Reddit / PTT / UDN / Google News / 自選股新聞）
-- ============================================================
create table if not exists public.news_items (
  id            bigint generated by default as identity primary key,
  source        text not null,        -- 'reddit','ptt','udn','gnews','portnews'
  category      text,                 -- subreddit name / 'gossiping' / 'finance' / stock_code
  ext_id        text,                 -- reddit.id / ptt url hash / rss guid
  title         text,
  title_zh      text,                 -- 若為英文來源，儲存已翻譯的繁中標題
  url           text,
  summary       text,
  published_at  timestamptz,
  author        text,
  score         int,                  -- reddit upvotes / ptt 推文數
  raw           jsonb,
  fetched_at    timestamptz not null default now(),
  unique (source, ext_id)
);
create index if not exists idx_news_items_source    on public.news_items(source);
create index if not exists idx_news_items_published on public.news_items(published_at desc);
create index if not exists idx_news_items_category  on public.news_items(category);


