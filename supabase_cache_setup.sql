-- ============================================================
-- Show Me The Money — Supabase 外部接口資料快取表
-- ------------------------------------------------------------
-- 本腳本為 idempotent（可重複執行），建立本專案所有對外 API
-- 的資料表 + RLS + 索引。資料表都是「公用快取」：
--   - 任何使用者（含匿名）皆可 SELECT
--   - 僅已登入使用者可 INSERT / UPDATE（避免匿名濫寫）
-- 使用方式：
--   1. 至 Supabase Dashboard → SQL Editor
--   2. 新建 query，貼上整份內容後按「Run」
--   3. 執行結果應看到「Success. No rows returned」
-- ============================================================

-- ============================================================
-- 1. 通用 API 快取（key/value 式，存任何 endpoint 的原始回應）
-- ============================================================
create table if not exists public.api_cache (
  id          bigint generated by default as identity primary key,
  source      text  not null,          -- 'twse'|'tpex'|'taifex'|'yf'|'goodinfo'|'reddit'|'ptt'|'udn'|'gnews'|'ndc'|'gtranslate'
  endpoint    text  not null,          -- normalized key，如 'T86:20260423'、'yfChart:2330.TW:1y'
  payload     jsonb not null,
  fetched_at  timestamptz not null default now(),
  ttl_seconds int   not null default 3600,
  expires_at  timestamptz
              generated always as
              (fetched_at + make_interval(secs => ttl_seconds)) stored,
  unique (source, endpoint)
);
create index if not exists idx_api_cache_source  on public.api_cache(source);
create index if not exists idx_api_cache_expires on public.api_cache(expires_at);


-- ============================================================
-- 2. 大盤指數 / 期貨（TAIEX、OTC、TXF、TXF_NIGHT）
-- ============================================================
create table if not exists public.market_indices (
  trade_date  date   not null,
  symbol      text   not null,         -- 'TAIEX','OTC','TXF','TXF_NIGHT'
  price       numeric(14,4),
  change      numeric(14,4),
  change_pct  numeric(8,4),
  volume_lots bigint,
  raw         jsonb,
  updated_at  timestamptz not null default now(),
  primary key (trade_date, symbol)
);
create index if not exists idx_market_indices_symbol on public.market_indices(symbol);


-- ============================================================
-- 3. 個股歷史日 K（TWSE STOCK_DAY + TPEX 合併）
-- ============================================================
create table if not exists public.stock_daily (
  trade_date  date   not null,
  stock_code  text   not null,
  open        numeric(12,4),
  high        numeric(12,4),
  low         numeric(12,4),
  close       numeric(12,4),
  volume      bigint,
  change      numeric(12,4),
  change_pct  numeric(8,4),
  source      text,                    -- 'twse' | 'tpex' | 'yf'
  raw         jsonb,
  updated_at  timestamptz not null default now(),
  primary key (trade_date, stock_code)
);
create index if not exists idx_stock_daily_code on public.stock_daily(stock_code);
create index if not exists idx_stock_daily_date on public.stock_daily(trade_date desc);


-- ============================================================
-- 4. 個股即時報價（MIS / TPEX）— 覆蓋式
-- ============================================================
